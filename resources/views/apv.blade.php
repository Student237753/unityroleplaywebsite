    <h1 class="text-4xl font-bold text-center mb-6">Algemene Plaatselijke Verordening Unity Roleplay</h1>

    <h2 class="text-2xl font-semibold mb-4">Strafbepaling</h2>
    <p class="mb-4">De straffen zijn onderverdeeld in acht categorieën:</p>

    <ul class="list-disc list-inside mb-4">
        <li>Categorie 1: een waarschuwing + 100 uur taakstraf *(max 3 waarschuwingen in 1 maand, hierna volgt de automatisch oplegging cat. 2)*</li>
        <li>Categorie 2: een ban van 1 dag</li>
        <li>Categorie 3: een ban van 2 dagen</li>
        <li>Categorie 4: een ban van 3 dagen</li>
        <li>Categorie 5: een ban van 1 week</li>
        <li>Categorie 6: een ban van 1 maand</li>
        <li>Categorie 7: permanente ban</li>
        <li>Categorie 8: account wipe</li>
    </ul>

    <p class="mb-4">De straffen van categorie 5, 6 en 7 kunnen preventief worden uitgedeeld wanneer sprake is van een overtreding op heterdaad. De opgelegde straf zal naderhand besproken worden binnen superadmin+ en waar nodig worden bijgesteld. Dit om eventuele verdere schade te beperken terwijl er gekeken wordt wat er precies gebeurt is.</p>

    <p class="mb-4">Er kan door een stafflid gekozen worden om straffen te verhogen en/of te verlagen op basis van de context, situatie of eerdere straffen/waarschuwingen van de overtreder in kwestie. Wanneer je het niet eens bent met een oordeel van een stafflid kan je hiervoor een ticket aanmaken.</p>
    <p class="mb-4">Een mondelinge waarschuwing dient in een notitie bij deze persoon aangegeven te zijn, anders is deze niet geldig. Het omzeilen van een straf zal resulteren in een permanente ban.</p>
    <p class="mb-4">Bans worden uitgedeeld op basis van de GTA licentie en IP-adres en geldt als zodanig voor alle gebruikers van deze kenmerken.</p>

    <p class="mb-4">Waarschuwingen zijn 3 maanden geldig, daarna vervallen ze automatisch.</p>

    <h2 class="text-2xl font-semibold mb-4">Overtredingen - Verstoring openbare orde (RP)</h2>

    <h3 class="text-xl font-semibold mb-2">Artikel 1 - Algemene Roleplay Richtlijnen</h3>
    <p class="mb-4">Het is verboden om een roleplay van zeer slechte kwaliteit uit te spelen, of roleplay verstorend gedrag te vertonen.</p>

    <ul class="list-disc list-inside mb-4">
        <li>Het is niet toegestaan om opzettelijk de roleplay zodanig te vormen dat er een oneerlijke of onrealistische draai aan het verhaal gegeven wordt, of de roleplay van de andere partij zelf invullen door een onrealistische dwang.</li>
        <li>Het is te allen tijde verboden om je karakter te breken. Hiermee bedoelen we dat je een term gebruikt die gezien wordt als OOC (Out Of Character).</li>
        <li>Ook is het niet toegestaan om “/looc” foutief te gebruiken of te gebruiken om toxic / zoutige opmerkingen te plaatsen.</li>
    </ul>

    <p class="mb-4">Enkele voorbeelden van het foutief gebruiken van looc:</p>
    <ul class="list-disc list-inside mb-4">
        <li>/looc wat doen jullie</li>
        <li>/looc waarom rdm je</li>
        <li>/looc slechte roleplay</li>
    </ul>

    <p class="mb-4">Het is verboden om door middel van de Safe Zone te ontkomen aan illegale activiteiten en/of roleplays. Het is verboden om een individu schade aan te richten, te ontvoeren, uitbreken, overvallen, aanrijden of vermoorden in een Safe Zone, tenzij er daadwerkelijke roleplay (die aan te tonen is) aan gekoppeld zit.</p>

    <p class="mb-4">Bij overtreding van lid 1 t/m 5 zal dit resulteren in een straf volgens de 1e categorie. Indien het karakter overleden is, wordt er verwacht dat hij/zij de laatste roleplay vergeten is. Het karakter mag ook niet naar het gebied waar hij/zij overleden is terugkeren, dit is aangeduid met een rode cirkel op de kaart voor 30 minuten na het overlijden.</p>

    <p class="mb-4">Tevens mag het karakter zich niet opnieuw bemoeien met ditzelfde scenario totdat deze afgelopen is (New life rule).</p>

    <p class="mb-4">Bij overtreding van lid 6 zal dit resulteren in een straf volgens de 4e categorie. Het onnodig mishandelen, vermoorden (RDM), uitlokken en/of anderen lastig vallen zonder verdere intentie tot roleplay is niet toegestaan. Dit geldt ook voor dieren.</p>

    <p class="mb-4">Hij/zij die geen waarde hecht aan het leven van het karakter dat hij/zij speelt (No Value of Life), zal bestraft worden. Wegrijden met een voertuig waarbij je trager dan 20 km/h rijdt valt hier ook onder.</p>

    <p class="mb-4">Bij overtreding van lid 7 en 8 zal dit resulteren in een straf volgens de 2e categorie.</p>

    <p class="mb-4">Het is verboden om een erotische roleplay (ERP) uit te voeren of aan te gaan. Bij overtreding zal dit resulteren in een straf volgens de 7e categorie.</p>

    <p class="mb-4">Het uitvoeren van een zelfmoordscenario is niet toegestaan, mits er goedkeuring voor is geweest vanuit staff. Bij geen goedkeuring zal dit resulteren in een straf van categorie 8 of 200 maanden celstraf.</p>

    <p class="mb-4">Enkele voorbeelden van lid 1 en lid 2 zijn:</p>
    <ul class="list-disc list-inside mb-4">
        <li>Tijdens een achtervolging je voertuig in de garage zetten om te voorkomen dat je je voertuig kwijt raakt;</li>
        <li>Een agent ontvoeren om een signalering te verwijderen of om iets te bekijken in het politiesysteem (MEOS).</li>
    </ul>

    <p class="mb-4">Medewerkers van de overheidsdiensten dwingen om een (bewusteloos) persoon te fouilleren, items (zoals wapens) af te nemen en af te geven aan jou of iemand anders. Het zogeheten "counteren" tijdens een politieachtervolging, dus het bemoeien in een achtervolging van een ander met de politie, is niet toegestaan.</p>

    <p class="mb-4">Ter verduidelijking: Indien de politie tussen een onderling conflict komt welke al bezig is, is deze regel niet van toepassing. Bij overtreding van lid 11 zal dit resulteren in een straf volgens de 2e categorie.</p>

    <h3 class="text-xl font-semibold mb-2">Artikel 2 - Cheats, Exploits & Meta-gaming</h3>
    <p class="mb-4">Hij/zij die gebruikmaakt van software/hulpmiddelen (cheats) van derde partijen om profijt te krijgen in het spel, wordt bestraft volgens de 7e categorie.</p>

    <p class="mb-4">Deze regel geldt ook voor de Discord-client zelf, dus gebruik van clients als BetterDiscord is niet toegestaan.</p>

    <p class="mb-4">Hij/zij die moedwillig een bug en/of exploit misbruikt om voor zichzelf of anderen profijt te behalen of in-game functionaliteiten omzeild, ongeacht in welke middelen, zal per direct gestraft worden volgens de 7e categorie.</p>

    <p class="mb-4">Informatie verkregen buiten het karakter om (alle informatie die niet verkregen is door jouw karakter binnen FiveM) mag niet gebruikt worden door het karakter. Bij overtreding zal gestraft worden volgens de 1e categorie.</p>

    <p class="mb-4">Straf zal hoger kunnen zijn aan de hand van de ernst van meta plegen.</p>

    <p class="mb-4">Indien je een incident hebt ervaren met een speler, een bug of een exploit dien je deze te melden bij staff. Bij het niet melden van deze incidenten, terwijl je hiervan op de hoogte bent, kan dit resulteren in een straf van categorie 8.</p>

    <h3 class="text-xl font-semibold mb-2">Artikel 3 - Regelgeving en misbruik</h3>
    <p class="mb-4">Het is niet toegestaan om anderen te benadelen in de breedste zin van het woord. Dit betekent dat je een ander in het echt of in de game geen ongepaste berichten stuurt, inclusief, maar niet beperkt tot, bedreigingen, intimidatie, haatzaaiende uitlatingen en pornografische content.</p>

    <p class="mb-4">Bij overtreding van lid 1 zal dit resulteren in een straf van categorie 7.</p>

    <p class="mb-4">Het is niet toegestaan om meer dan 1 account te gebruiken, of je zelf via andere accounts als "schuilnaam" in te loggen. Bij overtreding van lid 2 zal dit resulteren in een straf van categorie 8.</p>

    <h3 class="text-xl font-semibold mb-2">Artikel 4 - Medewerkers van Unity Roleplay</h3>
    <p class="mb-4">Iedereen dient de medewerkers van Unity Roleplay te respecteren en met hen samen te werken. Medewerkers zijn er om te helpen en niet om je te straffen. Bij gebrek aan samenwerking zal de medewerker in kwestie dit kunnen doorgeven aan de staff en leiden tot straf van categorie 3.</p>

    <p class="mb-4">Het is verboden om staffleden te benaderen in een discussie waar zij niet bij betrokken zijn, of andere staffleden te negeren wanneer deze in gesprek zijn met andere spelers.</p>

    <p class="mb-4">Bij overtreding van lid 3 kan de staf besluiten om een ban uit te delen van 1 week tot een maand.</p>

    <h3 class="text-xl font-semibold mb-2">Artikel 5 - Sluiten van tickets</h3>
    <p class="mb-4">Hij/zij die onterecht een ticket sluit kan bestraft worden met een waarschuwing. Bij herhaalde overtredingen kan dit resulteren in een straf van categorie 2.</p>

    <h2 class="text-2xl font-semibold mb-4">Toelichting</h2>
    <p class="mb-4">Indien je een vraag hebt over deze regels of niet akkoord gaat met een straf, kan je een ticket aanmaken om hierover in gesprek te gaan met staff. Deze regels zijn onderhevig aan veranderingen en updates. Houd rekening met ons Discord kanaal voor veranderingen.</p>
